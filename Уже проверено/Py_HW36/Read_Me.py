# ■ Отображение минимальной по сумме сделки для
# конкретного продавца; @
# ■ Отображение максимальной по сумме сделки для
# конкретного покупателя; @
# ■ Отображение минимальной по сумме сделки для
# конкретного покупателя; @
# ■ Отображение продавца, у которого максимальная
# сумма продаж по всем сделкам; @
# ■ Отображение продавца, у которого минимальная
# сумма продаж по всем сделкам; @
# ■ Отображение покупателя, у которого максимальная
# сумма покупок по всем сделкам;
# ■ Отображение средней суммы покупки для конкретного покупателя;
# ■ Отображение средней суммы покупки для конкретного продавца.

